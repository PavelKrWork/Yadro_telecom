import numpy as np

def decimate_2x(signal):
    """
    Простая децимация с понижением частоты дискретизации в 2 раза
    Использует простое прореживание с предварительным усреднением
    
    :param signal: входной сигнал (1D массив)
    :return: децимированный сигнал в 2 раза короче
    """
    # Простое усреднение соседних отсчетов для антиалиасинга
    filtered = (signal[:-1:2] + signal[1::2]) / 2
    
    # Если длина сигнала нечетная, добавляем последний отсчет
    if len(signal) % 2 != 0:
        filtered = np.append(filtered, signal[-1])
    
    return filtered