import numpy as np

def interpolate_2x(signal):
    """
    Простая интерполяция с повышением частоты дискретизации в 2 раза
    Использует линейную интерполяцию между отсчетами
    
    :param signal: входной сигнал (1D массив)
    :return: интерполированный сигнал в 2 раза длиннее
    """
    n = len(signal)
    # Создаем массив для увеличенного сигнала
    upsampled = np.zeros(2 * n - 1)
    
    # Вставляем исходные отсчеты на четные позиции
    upsampled[::2] = signal
    
    # Линейная интерполяция для нечетных позиций
    upsampled[1::2] = (signal[:-1] + signal[1:]) / 2
    
    return upsampled